class Tienda {
  String nombre;
  String telefono;
  String imagen;
  String descripcion;
  String website;
  bool recomendado;
  String idTienda;

  Tienda({
    required this.nombre,
    required this.telefono,
    required this.imagen,
    required this.descripcion,
    required this.website,
    required this.recomendado,
    required this.idTienda,
  });
}
